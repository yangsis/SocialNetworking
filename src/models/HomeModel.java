package models;

public class HomeModel {
	private String home_email;
	private String home_password;
	public String getHome_email() {
		return home_email;
	}
	public void setHome_email(String home_email) {
		this.home_email = home_email;
	}
	public String getHome_password() {
		return home_password;
	}
	public void setHome_password(String home_password) {
		this.home_password = home_password;
	}
		

}
