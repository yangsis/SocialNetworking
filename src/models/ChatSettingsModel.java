package models;

public class ChatSettingsModel {	
	private String chat_email;
	private String chat_password;
	private String partner;
	public String getChat_email() {
		return chat_email;
	}
	public void setChat_email(String chat_email) {
		this.chat_email = chat_email;
	}
	public String getChat_password() {
		return chat_password;
	}
	public void setChat_password(String chat_password) {
		this.chat_password = chat_password;
	}
	public String getPartner() {
		return partner;
	}
	public void setPartner(String partner) {
		this.partner = partner;
	}
	

}
