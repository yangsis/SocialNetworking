package models;

public class InboxModel {
	private String inbox_email;
	private String inbox_password;
	private String process_chat;
	public String getInbox_email() {
		return inbox_email;
	}
	public void setInbox_email(String inbox_email) {
		this.inbox_email = inbox_email;
	}
	public String getInbox_password() {
		return inbox_password;
	}
	public void setInbox_password(String inbox_password) {
		this.inbox_password = inbox_password;
	}
	public String getProcess_chat() {
		return process_chat;
	}
	public void setProcess_chat(String process_chat) {
		this.process_chat = process_chat;
	}
}
