package models;

public class FriendSearchModel {
	
	private String friendSearchModel_email;
	private String friendSearchModel_password;
	private String friendSearchModel_search_email;
	private String friendSearchModel_search_yes;
	public String getFriendSearchModel_email() {
		return friendSearchModel_email;
	}
	public void setFriendSearchModel_email(String friendSearchModel_email) {
		this.friendSearchModel_email = friendSearchModel_email;
	}
	public String getFriendSearchModel_password() {
		return friendSearchModel_password;
	}
	public void setFriendSearchModel_password(String friendSearchModel_password) {
		this.friendSearchModel_password = friendSearchModel_password;
	}
	public String getFriendSearchModel_search_email() {
		return friendSearchModel_search_email;
	}
	public void setFriendSearchModel_search_email(
			String friendSearchModel_search_email) {
		this.friendSearchModel_search_email = friendSearchModel_search_email;
	}
	public String getFriendSearchModel_search_yes() {
		return friendSearchModel_search_yes;
	}
	public void setFriendSearchModel_search_yes(
			String friendSearchModel_search_yes) {
		this.friendSearchModel_search_yes = friendSearchModel_search_yes;
	}

}
